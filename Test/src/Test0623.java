
public class Test0623 { //클래스 선

	public static void main(String[] args) { //메인 메소드 선언.
		
		System.out.println("서영우"); //화면으로 출력을 실행하는 프로그램
		/*
		 * maim 메소드 안의 프로그램 소스코드는 순차적으로실행된다.
		 * 
		 */
		System.out.print("서영우"); 
		/*
		 * print, println 의차이는 그냥 print는  줄바꿈을 하지않고 ln을 붙이면 줄바꿈을 한 상태로  출력. 
		 */
		System.out.println("서영우" + "건강은 어떠세요?"); // + 로 문자열을 연결합니다.
		System.out.println("안녕하세요, \n건강은 어떠세요"); // \n 줄바꿈 문자.
		
	}

}
